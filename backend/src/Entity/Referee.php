<?php
namespace App\Entity;

use Ramsey\Uuid\Uuid;

class Referee{
    private Uuid $id;
    private string $code;

    public function getId(): Uuid
    {
        return $this->id;
    }

    public function setId(Uuid $id): void
    {
        $this->id = $id;
    }

    public function getCode(): string
    {
        return $this->code;
    }

    public function setCode(string $code): void
    {
        $this->code = $code;
    }
}